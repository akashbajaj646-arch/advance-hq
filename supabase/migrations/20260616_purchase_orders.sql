-- Purchase Orders from ApparelMagic.
-- Run ONCE in the Supabase SQL editor. Idempotent (IF NOT EXISTS / safe to re-run).
--
-- Header keyed on apparel_magic_id (= AM purchase_order_id); items nest under it.
-- All access is via the service-role key (sync routes + /api/data), so RLS is
-- enabled with no policies = service-role-only, matching the rest of the app.

